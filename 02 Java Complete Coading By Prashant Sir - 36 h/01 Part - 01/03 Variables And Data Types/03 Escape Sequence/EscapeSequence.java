public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Hello Gaurav, \t What is your Roll Number: ");
        System.out.println("Hello Gaurav,\b What is your Roll Number: ");
        System.out.println("Hello Gaurav, \n What is your Roll Number: ");
        System.out.println("Hello Gaurav, \"What is your Roll Number\": ");
        System.out.println("Hello Gaurav, \'What is your Roll Number\': ");
        System.out.println("Hello Gaurav, \\What is your Roll Number\\: ");
    }
}
